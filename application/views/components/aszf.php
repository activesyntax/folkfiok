<h4>A megrendelés lépései:</h4>
<ol>
    <li>A termékeket a termék leírása mellett található "Kosárba" gomb megnyomásával helyezheti el a kosárban.</li>
    <li>A kosárban beállíthatja  kívánt mennyiséget és tovább válogathat.</li>
    <li>Miután összeállította rendelését, kattintson a "Rendelés" gombra, mely a kosárban található.</li>

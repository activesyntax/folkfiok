    <li>Rendelését a "Megrendelem" gomb megnyomásával véglegesítheti, miután meggyőződött róla, hogy valóban a megrendelni kívánt termékek vannak a kosarában és az Ön adatai is helyesen vannak feltüntetve az űrlapon.</li>
    <li>A Webáruház visszaigazoló emailt fog küldeni, az Ön által megadott email címre. </li>
    <li>Előfordulhat, hogy az Ön levelező rendszere a visszaigazoló emailt a levélszemét mappába (Spam) helyezi el. Kérjük, ellenőrizze ezt a mappát is, amennyiben a visszaigazoló emailt nem látja beérkező levelei között.</li>
    <li>A Webáruház a továbbiakban ezen az email címen értesíti Önt megrendelése akuális állapotáról. Kérjük </li>

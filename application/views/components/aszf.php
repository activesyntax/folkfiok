<h4>Jótállási vagy szavatossági igény bejelentésének módja</h4>
1. Személyes bejelentés<br>
A vásárló bejelentheti személyesen jótállási vagy szavatossági igényét a Szolgáltatónál (Szolgáltató címe). Ekkor az Szolgáltató munkatársa a kifogásáról jegyzőkönyvet vesz fel, amelyben rögzíti:<br>
- a vásárló nevét, címét, <br>
- a termék megnevezését, vételárát, <br>
- a vásárlás időpontját, <br>
- a hiba bejelentésének időpontját, 
- a hiba leírását, <br>
- a vásárló által érvényesíteni kívánt igényt, <br>
- a kifogás rendezésének módját. <br>

Ha a kifogás rendezésének módja a vásárló igényétől eltér, ennek indokolását a jegyzőkönyvben rögzíti. 
<br>
A jegyzőkönyv másolatát a vásárló átadja. 
<br><br>
2. Írásban történő bejelentés<br>
A vásárló bejelentheti jótállási vagy szavatossági igényét a Szolgáltatónak (Szolgáltató címe) küldött levélben, vagy elektronikus levélben (Szolgáltató email címe). <br>
Ekkor a levélnek tartalmaznia kell: <br>
- a vásárló nevét, címét, <br>
- a termék megnevezését, vételárát, <br>
- a vásárlás időpontját, <br>
- a hiba bejelentésének időpontját,<br> 
- a hiba leírását,<br>
- a vásárló által érvényesíteni kívánt igényt. <br>
